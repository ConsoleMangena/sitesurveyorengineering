#!/usr/bin/env node
/**
 * SiteSurveyor — one-time licensing setup.
 * ------------------------------------------
 * Run:  node scripts/setup-licensing.mjs            (first-time setup)
 *       node scripts/setup-licensing.mjs --rotate   (replace an existing key)
 *
 * What it does:
 *   1. Generates an Ed25519 keypair (or refuses to overwrite without --rotate).
 *   2. Writes the PUBLIC key to backend/.env.licensing (git-ignored) so
 *      `cargo build` / `tauri build` pick it up automatically — no manual env.
 *   3. Prints the PRIVATE seed ONCE and the exact `supabase secrets set`
 *      command. The seed is NEVER written to disk.
 *   4. Offers to run `supabase secrets set` for you if the CLI is available.
 *   5. Reminds you of the remaining one-time steps (SQL + function deploy).
 *
 * Security: the private seed is the only thing that can mint licenses. Keep it
 * out of source control and chat tools. This script never persists it.
 */

import crypto from 'node:crypto';
import fs from 'node:fs';
import path from 'node:path';
import readline from 'node:readline';
import { fileURLToPath } from 'node:url';
import { spawnSync } from 'node:child_process';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, '..');
const ENV_FILE = path.join(ROOT, 'backend', '.env.licensing');
const ROTATE = process.argv.includes('--rotate');

const EDGE_FUNCS = [
  'license-activate',
  'license-refresh',
  'license-admin-create',
  'license-admin-list',
  'license-admin-update',
];

function c(code, s) {
  return `\x1b[${code}m${s}\x1b[0m`;
}
const bold = (s) => c('1', s);
const green = (s) => c('32', s);
const yellow = (s) => c('33', s);
const red = (s) => c('31', s);
const cyan = (s) => c('36', s);

function generateKeypair() {
  const { publicKey, privateKey } = crypto.generateKeyPairSync('ed25519');
  const pubDer = publicKey.export({ type: 'spki', format: 'der' });
  const privDer = privateKey.export({ type: 'pkcs8', format: 'der' });
  return {
    publicHex: Buffer.from(pubDer.subarray(pubDer.length - 32)).toString('hex'),
    seedHex: Buffer.from(privDer.subarray(privDer.length - 32)).toString('hex'),
  };
}

function readExistingPublicKey() {
  if (!fs.existsSync(ENV_FILE)) return null;
  const txt = fs.readFileSync(ENV_FILE, 'utf8');
  const m = txt.match(/^SITESURVEYOR_LICENSE_PUBLIC_KEY=([0-9a-fA-F]{64})\s*$/m);
  return m ? m[1].toLowerCase() : null;
}

function writePublicKey(publicHex) {
  const content =
    '# SiteSurveyor licensing — PUBLIC key (safe to keep locally; git-ignored).\n' +
    '# Generated by scripts/setup-licensing.mjs. Do NOT put the private seed here.\n' +
    `SITESURVEYOR_LICENSE_PUBLIC_KEY=${publicHex}\n`;
  fs.writeFileSync(ENV_FILE, content, { mode: 0o600 });
}

function ask(question) {
  const rl = readline.createInterface({ input: process.stdin, output: process.stdout });
  return new Promise((resolve) => rl.question(question, (a) => { rl.close(); resolve(a.trim()); }));
}

function supabaseAvailable() {
  const probe = spawnSync('npx', ['supabase', '--version'], {
    cwd: ROOT, encoding: 'utf8', shell: process.platform === 'win32',
  });
  return probe.status === 0;
}

async function main() {
  console.log(bold('\n  SiteSurveyor licensing setup\n'));

  const existing = readExistingPublicKey();
  if (existing && !ROTATE) {
    console.log(yellow('  A licensing public key is already configured:'));
    console.log('    ' + existing);
    console.log(
      '\n  This is your one-time setup; nothing to do. To replace the key\n' +
      '  (e.g. it was exposed), re-run with ' + bold('--rotate') + '.\n' +
      '\n  ' + red('Warning:') + ' rotating invalidates all previously issued licenses;\n' +
      '  every client must re-activate online afterwards.\n'
    );
    printRemainingSteps();
    return;
  }

  if (existing && ROTATE) {
    const a = await ask(red('  Rotate the key? This invalidates all live licenses. Type "rotate" to confirm: '));
    if (a.toLowerCase() !== 'rotate') {
      console.log('\n  Aborted. No changes made.\n');
      return;
    }
  }

  const { publicHex, seedHex } = generateKeypair();
  writePublicKey(publicHex);

  console.log(green('  ✓ Public key written to ') + cyan('backend/.env.licensing'));
  console.log('    (the build reads it automatically — no env vars to remember)\n');

  console.log(bold('  PRIVATE SEED (shown once — store as a Supabase secret, never in a file):'));
  console.log('    ' + red(seedHex) + '\n');

  const secretCmd = `npx supabase secrets set LICENSE_PRIVATE_KEY_HEX=${seedHex}`;

  if (supabaseAvailable()) {
    const a = await ask('  Run the secret command for you now via the linked Supabase CLI? [y/N] ');
    if (a.toLowerCase() === 'y' || a.toLowerCase() === 'yes') {
      const res = spawnSync('npx', ['supabase', 'secrets', 'set', `LICENSE_PRIVATE_KEY_HEX=${seedHex}`], {
        cwd: ROOT, stdio: 'inherit', shell: process.platform === 'win32',
      });
      if (res.status === 0) {
        console.log(green('\n  ✓ Private seed set as a Supabase secret.\n'));
      } else {
        console.log(yellow('\n  Could not set the secret automatically. Run it manually:\n'));
        console.log('    ' + secretCmd + '\n');
      }
    } else {
      console.log('\n  Set it yourself with:\n    ' + secretCmd + '\n');
    }
  } else {
    console.log('  Set the private seed as a Supabase secret:\n    ' + secretCmd + '\n');
    console.log('  (Run `npx supabase login` and `npx supabase link` first if needed.)\n');
  }

  printRemainingSteps();
}

function printRemainingSteps() {
  console.log(bold('  Remaining one-time steps:'));
  console.log('    1. Run the SQL in the Supabase SQL editor, in this order:');
  console.log('         backend/supabase/sql/01_schema.sql');
  console.log('         backend/supabase/sql/02_functions_triggers.sql');
  console.log('         backend/supabase/sql/03_rls_storage.sql');
  console.log('         backend/supabase/sql/04_seed.sql');
  console.log('         backend/supabase/sql/06_licensing.sql');
  console.log('       (See backend/supabase/sql/README.md. No extra migration scripts are needed.)');
  console.log('    2. Provision your admin account(s) (SQL editor):');
  console.log("         UPDATE profiles SET is_platform_admin = true WHERE lower(email) = 'you@example.com';");
  console.log('    3. Deploy the Edge Functions (run from backend/):');
  console.log('         cd backend');
  console.log('         npx supabase functions deploy ' + EDGE_FUNCS.join(' '));
  console.log('    4. In the Supabase dashboard, require "Confirm email" under Authentication.');
  console.log('\n  After that: build normally and issue licenses from the admin dashboard.\n');
}

main().catch((e) => {
  console.error(red('\n  Setup failed: ') + (e?.message || e) + '\n');
  process.exit(1);
});
